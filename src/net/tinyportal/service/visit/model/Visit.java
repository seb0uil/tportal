package net.tinyportal.service.visit.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

import com.google.appengine.api.datastore.Key;

@Entity
public class Visit {
	@Id @GeneratedValue(strategy = GenerationType.IDENTITY)
	private Key key;
	private String name;
	private String mail;
	private Boolean verifiedMail;
	private String givenName;
	private String familyName;
	private String birthday;
	private Date visit;

	/*Getter & Setter */
	public Key getKey() {
		return key;
	}
	public void setKey(Key key) {
		this.key = key;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public Boolean getVerifiedMail() {
		return verifiedMail;
	}
	public void setVerifiedMail(Boolean verifiedMail) {
		this.verifiedMail = verifiedMail;
	}
	public String getGivenName() {
		return givenName;
	}
	public void setGivenName(String givenName) {
		this.givenName = givenName;
	}
	public String getFamilyName() {
		return familyName;
	}
	public void setFamilyName(String familyName) {
		this.familyName = familyName;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public Date getVisit() {
		return visit;
	}
	public void setVisit(Date visit) {
		this.visit = visit;
	}

}
